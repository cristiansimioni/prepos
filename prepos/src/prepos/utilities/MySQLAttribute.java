public class MySQLAttribute {
    // Attribute

    private String name;
    private String type;
    private int lenght;
    private int start;
    private int end;

    // Constructor
    public MySQLAttribute(String name, String type, int start, int end) {
        this.name = name;
        this.type = type;
        this.start = start;
        this.end = end;
    }

    // Getter & setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getLenght() {
        return lenght;
    }

    public void setLenght(int lenght) {
        this.lenght = lenght;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
